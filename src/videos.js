function videosSlide1(){
    let black_line = document.getElementsByClassName("black-line")
    let black_line_first = document.getElementsByClassName("black-line-first")

    let design_installation_power_on = document.getElementsByClassName("video-design-installation-power-on")
    let design_installation_power_on_first = document.getElementsByClassName("video-design-installation-power-on-first")

    let navigation = document.getElementsByClassName("navigations")  
    let navigation_first = document.getElementsByClassName("navigation-first")

    design_installation_power_on_first[0].style.display='block'
    design_installation_power_on[0].style.display='none'
    design_installation_power_on[1].style.display='none'

    black_line_first[0].style.backgroundColor='#171A20'
    black_line[0].style.backgroundColor='#A2A3A6'
    black_line[1].style.backgroundColor='#A2A3A6'

    black_line_first[0].style.height='3px'
    black_line[0].style.height='2px'
    black_line[1].style.height='2px'

    navigation_first[0].style.color='#171A20'
    navigation[0].style.color='#A2A3A6'
    navigation[1].style.color='#A2A3A6'
}
